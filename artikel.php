    <h4 class="media-heading"><b>Apa itu kanker Paru - Paru?</b></h4>
    <p>
Kanker paru-paru adalah kondisi ketika sel ganas (kanker) terbentuk di paru-paru. Kanker ini lebih banyak dialami oleh orang yang memiliki kebiasaan merokok dan merupakan satu dari tiga jenis kanker yang paling banyak terjadi di Indonesia.</p><p>
Walaupun sering terjadi pada perokok, kanker paru-paru juga bisa terjadi pada orang yang bukan perokok, terutama pada orang yang sering terpapar zat kimia di lingkungan kerjanya atau terpapar asap rokok dari orang lain.
</p>
