    <h4 class="media-heading"><b>Apa itu kanker mulut?</b></h4>
    <p>
Kanker mulut adalah perkembangan sel-sel kanker yang menyerang jaringan mulut. Awalnya, kanker ini tidak langsung tumbuh begitu saja, melainkan didahului dengan munculnya luka di mulut yang tidak kunjung sembuh. Kanker mulut merupakan satu dari beberapa jenis kanker yang masuk dalam kategori kanker leher dan kepala, sehingga tindakan perawatannya pun serupa.</p>
<p>Meski namanya kanker mulut, tapi jenis kanker yang juga dikenal dengan nama kanker oral ini tidak hanya berkembang di mulut saja. Sel-sel kanker juga bisa muncul di area sekitar mulut, seperti lidah, bibir, pipi, gusi, sinus, hingga tenggorokan.
</p>
